<template>
  <div class="login-container">
    <el-form ref="loginForm" :model="loginForm" :rules="loginRules" class="login-form" auto-complete="on" label-position="left">

      <div class="title-container">
        <h3 class="title">XXXXXX系统</h3>
      </div>

      <el-form-item prop="username">
        <span class="svg-container">
          <svg-icon icon-class="user" />
        </span>
        <el-input
          ref="username"
          v-model="loginForm.username"
          placeholder="用户名"
          name="username"
          type="text"
          tabindex="1"
          auto-complete="on"
        />
      </el-form-item>

      <el-form-item prop="password">
        <span class="svg-container">
          <svg-icon icon-class="password" />
        </span>
        <el-input
          :key="passwordType"
          ref="password"
          v-model="loginForm.password"
          :type="passwordType"
          placeholder="密码"
          name="password"
          tabindex="2"
          auto-complete="on"
          @keyup.enter.native="handleLogin"
        />
        <span class="show-pwd" @click="showPwd">
          <svg-icon :icon-class="passwordType === 'password' ? 'eye' : 'eye-open'" />
        </span>
      </el-form-item>

      
      <div style="display:flex;display:-webkit-flex;justify-content:space-around">

        <el-button :loading="loading" type="primary" style="width:50%" @click.native.prevent="handleLogin" >登录</el-button>
        <el-button style="width:50%" @click.native.prevent="dialogVisible = true">注册</el-button>
      
      </div>
    </el-form>
    <el-dialog class="registerForm"  :visible.sync="dialogVisible"  :fullscreen="fullscreen" :center=true :close-on-click-modal=false>
      <el-form :model="registerForm"  style="margin:0 auto">
        <div class="title-container">
          <h4 class="title">注册</h4>
        </div>
        <el-form-item  >
          <el-input  autocomplete="off" v-model="registerForm.username" placeholder="用户名"></el-input>
        </el-form-item>

        <el-form-item  >
          <el-input  autocomplete="off" type="password" v-model="registerForm.password" placeholder="密码"></el-input>
        </el-form-item>

        <el-form-item  >
          <el-input  autocomplete="off" v-model="registerForm.password2" placeholder="再次输入密码"></el-input>
        </el-form-item>
      </el-form>

      <div slot="footer">
        <el-button type="primary" style="width:100%">确定</el-button>
      </div>
      
    </el-dialog>
  </div>
  
</template>

<script>
import { validUsername } from '@/utils/validate'

export default {
  name: 'Login',
  data() {
    let regEn = /[`~!@#$%^&*()_+<>?:"{},.\/;'[\] ]/im,
        regCn = /[·！#￥（——）：；“”‘、，|《。》？、【】[\]]/im 
    const validateUsername = (rule, value, callback) => {
      

      if (value.length<=0) {
        callback(new Error('请输入用户名'))
      }else if(regEn.test(value) || regCn.test(value)){
        callback(new Error('用户名中不允许输入特殊字符'))
      } else {
        callback()
      }
    }
    const validatePassword = (rule, value, callback) => {
      if(value.length <= 0){
        callback(new Error('密码不能为空'))
      }else if (value.length < 6) {
        callback(new Error('密码不能少于6位'))
      }else if (value.length > 20) {
        callback(new Error('密码不能大于20位'))
      }else if(regEn.test(value) || regCn.test(value)){
        callback(new Error('用户名中不允许输入特殊字符'))
      } else {
        callback()
      }
    }
    return {
      loginForm: {
        username: '',
        password: ''
      },
      registerForm:{
        username: '',
        password: '',
        password2: '',
      },
      loginRules: {
        username: [{ required: true, trigger: 'blur', validator: validateUsername }],
        password: [{ required: true, trigger: 'blur', validator: validatePassword }]
      },
      loading: false,
      passwordType: 'password',
      redirect: undefined,
      dialogVisible:false,
      fullscreen:false,
      screenWidth:document.body.clientWidth,
    }
  },
  watch: {
    $route: {
      handler: function(route) {
        this.redirect = route.query && route.query.redirect
      },
      immediate: true
    }
  },
  mounted() {
    if(this.screenWidth < 768){
      this.fullscreen = true
    }
    
  },
  methods: {
    showPwd() {
      if (this.passwordType === 'password') {
        this.passwordType = ''
      } else {
        this.passwordType = 'password'
      }
      this.$nextTick(() => {
        this.$refs.password.focus()
      })
    },
    handleLogin() {
      this.$refs.loginForm.validate(valid => {
        if (valid) {
          this.loading = true
          this.$store.dispatch('Login', this.loginForm).then(() => {
            this.$router.push({ path: this.redirect || '/' })
            this.loading = false
          }).catch(() => {
            this.loading = false
          })
        } else {
          console.log('error submit!!')
          return false
        }
      })
    }
  }
}
</script>

